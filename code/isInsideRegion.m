function yn = isInsideRegion(p, R)

    r = R;

    for j=1:4
        r(j,:) = r(j,:) - p;
    end

    
    p1 = r(1,:);
    p2 = r(2,:);
    p3 = r(3,:);
    p4 = r(4,:);
    
    a1 = 0.5*abs(p1(1)*p2(2) - p1(2)*p2(1));
    a2 = 0.5*abs(p2(1)*p3(2) - p2(2)*p3(1));
    a3 = 0.5*abs(p3(1)*p4(2) - p3(2)*p4(1));
    a4 = 0.5*abs(p4(1)*p1(2) - p4(2)*p1(1));
    
    at = a1+a2+a3+a4;
    
    a1 = (p2(2)+p1(2))*(p2(1)-p1(1))/2;
    a2 = (p3(2)+p2(2))*(p3(1)-p2(1))/2;
    a3 = (p4(2)+p3(2))*(p4(1)-p3(1))/2;
    a4 = (p1(2)+p4(2))*(p1(1)-p4(1))/2;
    
    aq = abs(a1+a2+a3+a4);
    
    yn = abs(at-aq)<1e-10;
end